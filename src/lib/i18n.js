export function t(locale, key) {
  return translations[locale]?.[key] ?? translations.en[key] ?? key;
}

export function localizeDescription(description, locale) {
  if (!description) return {};
  return (
    description[locale] ??
    description.en ??
    description[Object.keys(description)[0]] ??
    {}
  );
}
